package com.namani.demo.dto;

public class Location {
    float x;
    float y;
    int floor;

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public int getFloor() {
        return floor;
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }

    public void setFloor(int floor) {
        this.floor = floor;
    }
}
